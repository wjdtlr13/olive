package com.olive.olive.papers.tips;

public class Tips {
	private int num, listNum;
	private String userId;
	private String userName;
	private String subject;
	private String content;
	private String created;
	private int hitCount;
	
	private int tipsLikcCount;
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getListNum() {
		return listNum;
	}
	public void setListNum(int listNum) {
		this.listNum = listNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public int getHitCount() {
		return hitCount;
	}
	public void setHitCount(int hitCount) {
		this.hitCount = hitCount;
	}
	public int getTipsLikcCount() {
		return tipsLikcCount;
	}
	public void setTipsLikcCount(int tipsLikcCount) {
		this.tipsLikcCount = tipsLikcCount;
	}
	
	
}
